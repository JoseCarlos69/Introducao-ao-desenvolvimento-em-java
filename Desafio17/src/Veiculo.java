
public class Veiculo {
	private String placa;
	private String cor;
	private int numDePassageiros;
	private int capacidadeDoTanque;
	private float velocidadeMaxima;
	private float velocidadeAtual;
	private int consumoMedio;
	
	public String getPlaca() {
		return placa;
	}
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	public String getCor() {
		return cor;
	}
	public void setCor(String cor) {
		this.cor = cor;
	}
	public int getNumDePassageiros() {
		return numDePassageiros;
	}
	public void setNumDePassageiros(int numDePassageiros) {
		this.numDePassageiros = numDePassageiros;
	}
	public int getCapacidadeDoTanque() {
		return capacidadeDoTanque;
	}
	public void setCapacidadeDoTanque(int capacidadeDoTanque) {
		this.capacidadeDoTanque = capacidadeDoTanque;
	}
	public float getVelocidadeMaxima() {
		return velocidadeMaxima;
	}
	public void setVelocidadeMaxima(float velocidadeMaxima) {
		this.velocidadeMaxima = velocidadeMaxima;
	}
	public float getVelocidadeAtual() {
		return velocidadeAtual;
	}
	public void setVelocidadeAtual(float velocidadeAtual) {
		this.velocidadeAtual = velocidadeAtual;
	}
	public int getConsumoMedio() {
		return consumoMedio;
	}
	public void setConsumoMedio(int consumoMedio) {
		this.consumoMedio = consumoMedio;
	}
}
